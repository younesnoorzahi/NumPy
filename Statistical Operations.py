import numpy as np

# Create an array
arr = np.array([1, 2, 3, 4, 5])

# Mean
print("Mean:", np.mean(arr))

# Median
print("Median:", np.median(arr))

# Standard Deviation
print("Standard Deviation:", np.std(arr))

# Sum
print("Sum:", np.sum(arr))

# Min and Max
print("Min:", np.min(arr))
print("Max:", np.max(arr))
